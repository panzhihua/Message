package com.rongyan.hpmessage.item;

public class AdsItem {
	
	private int id;//广告Id
	
	private int time;//广告上一次弹出时间
	
	private int number;//广告弹出次数

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}	

}
